#ifndef CHAROBJECT_H
#define CHAROBJECT_H

#include "Helpers.h"
#include "Settings.h"
#include "Vector2.h"

//=============================================================
// Class Name   : CharObject
//-------------------------------------------------------------
// Purpose      : Hold data about position, char and 
//				  text attribute
//-------------------------------------------------------------
// Used in      : Border.h
//				  CharObject.cpp     
//				  Fruit.h
//				  Snake.h
//=============================================================
class CharObject
{
protected:
	char m_char; //Holds a character or "graphic" that will be displayed
	Vector2 m_position; //Holds x and y coordinate as 2D position (see Vector2.h/cpp)
	int m_textAttribute; //Holds text attribute which determines the color of m_char

public:

	//========================================================
    // Function Name	: CharObject()
    //--------------------------------------------------------
    // Parameters		: -
    //--------------------------------------------------------
    // Purpose			: Default CharObject constructor
    //--------------------------------------------------------
    // Returns			: -
    //========================================================
	CharObject();

	//========================================================
    // Function Name	: CharObject()
    //--------------------------------------------------------
    // Parameters		: Char to display
	//					  Position X
	//					  Position Y
	//					  Char color
    //--------------------------------------------------------
    // Purpose			: CharObject constructor
    //--------------------------------------------------------
    // Returns			: -
    //========================================================
    CharObject( char objChar, int x, int y, int textAttribute );

	//========================================================
	// Function Name	: ~CharObject()
	//--------------------------------------------------------
	// Parameters		: -
	//--------------------------------------------------------
	// Purpose			: CharObject destructor
	//--------------------------------------------------------
	// Returns			: -
	//========================================================
	~CharObject();

	//========================================================
	// Function Name	: draw()
	//--------------------------------------------------------
	// Parameters		: -
	//--------------------------------------------------------
	// Purpose			: Draws the char on the screen
	//--------------------------------------------------------
	// Returns			: -
	//========================================================	
	virtual void draw();

	//========================================================
	// Function Name	: erase()
	//--------------------------------------------------------
	// Parameters		: -
	//--------------------------------------------------------
	// Purpose			: Draws empty space in chars position
	//--------------------------------------------------------
	// Returns			: -
	//========================================================
	virtual void erase();

	//========================================================
	// Function Name	: setPosition()
	//--------------------------------------------------------
	// Parameters		: Vector2 position (see Vector2.h/cpp)
	//--------------------------------------------------------
	// Purpose			: Set chars position using Vector2
	//--------------------------------------------------------
	// Returns			: -
	//========================================================	
	void setPosition( Vector2 position );

	//========================================================
	// Function Name	: setPosition()
	//--------------------------------------------------------
	// Parameters		: Position X
	//					: Position Y
	//--------------------------------------------------------
	// Purpose			: Set chars position using two ints
	//--------------------------------------------------------
	// Returns			: -
	//========================================================
	void setPosition( int xValue, int yValue );

	//========================================================
	// Function Name	: initialize()
	//--------------------------------------------------------
	// Parameters		: Char to display
	//					  Position X
	//					  Position Y
	//					  Char color
	//--------------------------------------------------------
	// Purpose			: Initialize all class values 
	//--------------------------------------------------------
	// Returns			: -
	//========================================================
	void initialize( char objChar, int x, int y, int textAttribute );

	//========================================================
	// Function Name	: setTextAttribute()
	//--------------------------------------------------------
	// Parameters		: Char color
	//--------------------------------------------------------
	// Purpose			: Set char color
	//--------------------------------------------------------
	// Returns			: -
	//========================================================
	void setTextAttribute( int textAttribute );

	//========================================================
	// Function Name	: getVector2()
	//--------------------------------------------------------
	// Parameters		: -
	//--------------------------------------------------------
	// Purpose			: Return position
	//--------------------------------------------------------
	// Returns			: Vector2 position (see Vector2.h/cpp)
	//========================================================
	Vector2 getVector2();

	//========================================================
    // Function Name	: getX()
    //--------------------------------------------------------
    // Parameters		: -
    //--------------------------------------------------------
    // Purpose			: Return x coordinate
    //--------------------------------------------------------
    // Returns			: int x coordinate
    //========================================================	
	int getX() const;

	//========================================================
    // Function Name	: getY()
    //--------------------------------------------------------
    // Parameters		: -
    //--------------------------------------------------------
    // Purpose			: Return y coordinate
    //--------------------------------------------------------
    // Returns			: int y coordinate
    //========================================================
	int getY() const;

	//========================================================
    // Function Name	: setX()
    //--------------------------------------------------------
    // Parameters		: Position x
    //--------------------------------------------------------
    // Purpose			: Set x coordinate
    //--------------------------------------------------------
    // Returns			: -
    //========================================================		
	void setX( int value );

	//========================================================
    // Function Name	: setY()
    //--------------------------------------------------------
    // Parameters		: Position y
    //--------------------------------------------------------
    // Purpose			: Set y coordinate
    //--------------------------------------------------------
    // Returns			: -
    //========================================================
	void setY( int value );
};
#endif