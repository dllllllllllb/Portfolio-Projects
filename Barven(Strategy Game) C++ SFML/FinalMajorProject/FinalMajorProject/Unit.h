#ifndef UNIT_H
#define UNIT_H

#include <SFML/Graphics/Sprite.hpp>

#include "UnitData.h"

//===========================================================================================
//Description: Stores individual unit data
//===========================================================================================
class Unit
{
protected:

	const UnitData* m_pBaseUnitData;

	int m_numOfUnits;

	int m_health;
	int m_currentHealth;
	int m_attack;
	int m_ammunition;
	int m_range;
	int m_defence;
	int m_speed;

	bool m_isDataSet;

public:
	//===========================================================================================
	//Description: Class constructor
	//===========================================================================================
	Unit();

	//===========================================================================================
	//Description: Class destructor
	//===========================================================================================
	~Unit();

	//===========================================================================================
	//Description: Sets number of units
	//===========================================================================================
	void setNumOfUnits(const int& value);

	//===========================================================================================
	//Description: SIncrements number of units by passed value
	//===========================================================================================
	void incrementNumOfUnits(const int& value);

	//===========================================================================================
	//Description: Returns number of units
	//===========================================================================================
	const int& getNumOfUnits() const;

	//===========================================================================================
	//Description: Sets unit specific data
	//===========================================================================================
	void setUnitSpecificData(const UnitBattleDataEnum& dataType, const int& value);

	//===========================================================================================
	//Description: Return unit specific data
	//===========================================================================================
	const int& getUnitSpecificData(const UnitBattleDataEnum& dataType);

	//===========================================================================================
	//Description: Initialized unit specific data using unit base data
	//===========================================================================================
	void initializeUnitSpecificData();

	//===========================================================================================
	//Description: Resets current healt to max health
	//===========================================================================================
	void resetCurrentHealth();

	//===========================================================================================
	//Description: Sets current healt
	//===========================================================================================
	void setCurrentHealth(const int& value);

	//===========================================================================================
	//Description: Returns current health
	//===========================================================================================
	const int& getCurrentHealth() const;

	//===========================================================================================
	//Description: Increments current health
	//===========================================================================================
	void incrementCurrentHealth(const int& value);

	//===========================================================================================
	//Description: Sets base unit data
	//===========================================================================================
	void setBaseUnitData(const UnitData* baseUnitData);

	//===========================================================================================
	//Description: Returns base unit data
	//===========================================================================================
	const UnitData* getBaseUnitData() const;

	//===========================================================================================
	//Description: Bool that determines if data was set to this data container
	//===========================================================================================
	void setIsUnitDataSet(const bool state);

	//===========================================================================================
	//Description: Toggles between true and false to determine if data was set to this data container
	//===========================================================================================
	void toggleIsDataSet();

	//===========================================================================================
	//Description: Returns a bool if data was set to this container
	//===========================================================================================
	const bool getIsDataSet() const;

	//===========================================================================================
	//Description: Resets unit data
	//===========================================================================================
	void resetUnitData();
};
#endif // !UNIT_H
