#ifndef MAPAIHERODATA_H
#define MAPAIHERODATA_H

#include "MapAIPriorityEnum.h"

//===========================================================================================
//Description: Holds heros AI priority data 
//===========================================================================================
class MapAIHeroData
{
private:
	int m_heroIndex;
	int m_priorityObjectIndex;
	int m_priorityTileIndex;
	MapAIPriorityEnum m_priority;

public:
	//===========================================================================================
	//Description: Class constructor
	//===========================================================================================
	MapAIHeroData();

	//===========================================================================================
	//Description: Class destructor
	//===========================================================================================
	~MapAIHeroData();

	//===========================================================================================
	//Description: Set hero index
	//===========================================================================================
	void setHeroIndex(const int& index);

	//===========================================================================================
	//Description: Return hero index
	//===========================================================================================
	const int& getHeroIndex() const;

	//===========================================================================================
	//Description: Set object priority vector index
	//===========================================================================================
	void setPriorityObjectIndex(const int& index);

	//===========================================================================================
	//Description: Return object priority vector index
	//===========================================================================================
	const int& getPriorityObjectIndex() const;

	//===========================================================================================
	//Description: Set priority tile index
	//===========================================================================================
	void setPriorityTileIndex(const int& index);

	//===========================================================================================
	//Description: Return priority tile index
	//===========================================================================================
	const int& getPriorityTileIndex() const;

	//===========================================================================================
	//Description: Set AIs priority
	//===========================================================================================
	void setPriority(const MapAIPriorityEnum& priority);

	//===========================================================================================
	//Description: Return AIs priority
	//===========================================================================================
	const MapAIPriorityEnum getPriority() const;

	//===========================================================================================
	//Description: Reset AIs priority, vector index and tile index
	//===========================================================================================
	void resetPriority();
};

#endif // !MAPAIHERODATA_H
