#ifndef TEXTBOXTITLE_H
#define TEXTBOXTITLE_H

#include <SFML/Graphics/Text.hpp>

#include "Fonts.h"
#include "UIElement.h"

//===========================================================================================
//Description: Title for a text box
//===========================================================================================
class TextBoxTitle : public virtual UIElement
{
protected:
	Fonts& m_fonts;
	sf::Text m_titleText;

public:

	//===========================================================================================
	//Description: Class constructor
	//===========================================================================================
	TextBoxTitle(sf::RenderWindow& window, Textures& rTextures, Fonts& rFonts);

	//===========================================================================================
	//Description: Class destructor
	//===========================================================================================
	~TextBoxTitle();

	//===========================================================================================
	//Description: Sets title to the centre, top of the UI element
	//===========================================================================================
	void setDefaultTitlePosition();

	//===========================================================================================
	//Description: Set custom title position
	//===========================================================================================
	void setTitlePosition(const float& x, const float& y);

	//===========================================================================================
	//Description: Sets title string and char size
	//===========================================================================================
	void setUpTitleText(const sf::String& titleText, const int& charSize);

	//===========================================================================================
	//Description: Draws title text
	//===========================================================================================
	void draw();

	//===========================================================================================
	//Description: Returns title text reference
	//===========================================================================================
	sf::Text& getTitleText();

	//===========================================================================================
	//Description: Returns height of title text
	//===========================================================================================
	const float& getTitleHeight() const;
};

#endif // !TEXTBOXTITLE_H
